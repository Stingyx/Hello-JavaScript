/*  Variables de JavaScript
    Formas de crear una variable
*/

//var
var helloWorld = "¡Hola, JavaScript!"
console.log(helloWorld)


var helloWorld = "¡Hola de nuevo, JavaScript!"
console.log(helloWorld)

//let Este tipo de variables pueden variar en su contenido, por eso es posible reasignar la variable
let helloWorld2 = "¡Hola, JavaScript 2!"
console.log(helloWorld2)

helloWorld2 = "¡Hola de nuevo, JavaScript 2!"
console.log(helloWorld2)

//const     Con este tipo de variable no se puede reasignar, por eso manda error al querer cambiar el contenido de la variable, una variable estática,se queda con lo que se define desde el inicio
const  helloWorld3 = "Hola, JavaScript 3!"
console.log(helloWorld3)

